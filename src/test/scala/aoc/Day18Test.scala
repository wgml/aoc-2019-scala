package aoc

import org.scalatest.FunSuite

class Day18Test extends FunSuite {
  test("Day18.first") {
    assert(Day18.first("#########\n#b.A.@.a#\n#########".split('\n')) === 8)
    assert(Day18.first("########################\n#f.D.E.e.C.b.A.@.a.B.c.#\n######################.#\n#d.....................#\n########################".split('\n')) === 86)
    assert(Day18.first("########################\n#...............b.C.D.f#\n#.######################\n#.....@.a.B.c.d.A.e.F.g#\n########################".split('\n')) === 132)
    assert(Day18.first("#################\n#i.G..c...e..H.p#\n########.########\n#j.A..b...f..D.o#\n########@########\n#k.E..a...g..B.n#\n########.########\n#l.F..d...h..C.m#\n#################".split('\n')) === 136)
    assert(Day18.first("########################\n#@..............ac.GI.b#\n###d#e#f################\n###A#B#C################\n###g#h#i################\n########################".split('\n')) === 81)
  }
}
